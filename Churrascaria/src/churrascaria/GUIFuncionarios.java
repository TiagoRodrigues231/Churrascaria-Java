    public static void atualizaTabela() {
        DefaultTableModel tTabela = (DefaultTableModel) jTable4.getModel();
        tTabela.setNumRows(0);

        ArrayList<Funcionario> funcionarios = Banco.getFuncionarios();

        for (int linha = 0; linha < funcionarios.size(); linha++) {
            Funcionario funcionario = funcionarios.get(linha);

            tTabela.addRow(new Object[]{1});

            jTable4.setValueAt(funcionario.getCodigo(), linha, 0);
            jTable4.setValueAt(funcionario.getNome(), linha, 1);
            jTable4.setValueAt(funcionario.getCpf(), linha, 2);
            jTable4.setValueAt(funcionario.getSalario(), linha, 3);
            jTable4.setValueAt(funcionario.getDataContratacao(), linha, 4);
            jTable4.setValueAt(funcionario.getEndereco().getRua(), linha, 5);
            jTable4.setValueAt(funcionario.getEndereco().getBairro(), linha, 6);
            jTable4.setValueAt(funcionario.getEndereco().getCidade(), linha, 7);
            jTable4.setValueAt(funcionario.getEndereco().getCep(), linha, 8);
        }

    }

